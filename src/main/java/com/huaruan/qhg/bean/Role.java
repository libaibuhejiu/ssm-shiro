package com.huaruan.qhg.bean;

/**
 * 角色类
 * @author yangjian
 *
 */
public class Role {

	private int id;
	private String roleId;
	private String role;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRole() {
		return role;
	}
	public void setRoleName(String role) {
		this.role = role;
	}
	public String getRoleId() {
		return roleId;
	}
	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}
	
	
}
